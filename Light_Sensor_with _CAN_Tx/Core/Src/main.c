void Light_sensor_init(void)
{
	cmd=0x01; //Power On
    HAL_I2C_Master_Transmit(&hi2c2, SLAVE_ADDR, &cmd, 1, HAL_MAX_DELAY);
    HAL_Delay(50);

    cmd=0x10;
    HAL_I2C_Master_Transmit(&hi2c2, SLAVE_ADDR, &cmd, 1, HAL_MAX_DELAY);
    HAL_Delay(180);  //continuous mode 120ms
}

